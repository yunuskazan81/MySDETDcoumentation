package com.class2;

public class VariablesDeclaration {

	public static void main(String[] args) {

		// different ways to create/declare variables;
		// 1. create variable and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable
		y = 12; // 2. assign value

		// declare 1 by 1
		int a;
		int b;
		int c;
		int d;
		int f;
		
		a=10;
		b=11;
		c=12;
		// or declare all together
		int x, m, z, w, v;

		a = 10;
		b = 20;

		int aa = 10;
		int bb = 20;
		String name = "John";
		System.out.println(name);

		// for mac cmd+shift+f = formatting
		// windows ctrl_shift+f = formatting
		
	}
}
