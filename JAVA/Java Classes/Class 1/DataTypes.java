package com.class1;

public class DataTypes {

	public static void main(String[] args) {
		
		//creating variables: type, name, value
		//numbers family
		byte b=-128;
		short s=3276;
		int i=678769;
		long l=7587558578667l;
		
		//numbers with decimals
		float f=2.2f;
		double d=3.7685665465;
		
		//character
		char c='a';
		
		//boolean (true or false)
		boolean g=false;
		
		//String
		String str="A123%$%#";
		
		System.out.println(b);

	}
}
